public class Student {
    // Step 1: Private data members
    private int rollNo;
    private String name;

    // Step 2: Getter for rollNo
    public int RollNo() {
        return rollNo;
    }

    // Step 3: Setter for rollNo
    public void setRollNo(int rollNo) {
        this.rollNo = rollNo;
    }

    // Step 4: Getter for name
    public String Name() {
        return name;
    }

    // Step 5: Setter for name
    public void setName(String name) {
        this.name = name;
    }
}
